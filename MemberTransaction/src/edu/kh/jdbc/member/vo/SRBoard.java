package edu.kh.jdbc.member.vo;

public class SRBoard {

	private int srBoardNo;
	private int memberNo;
	private String sellerRight;
	private String phone;
	
	public SRBoard() {}

	public int getSrBoardNo() {
		return srBoardNo;
	}

	public void setSrBoardNo(int srBoardNo) {
		this.srBoardNo = srBoardNo;
	}

	public int getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}

	public String getSellerRight() {
		return sellerRight;
	}

	public void setSellerRight(String sellerRight) {
		this.sellerRight = sellerRight;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	
	
	
	
	
	
}
